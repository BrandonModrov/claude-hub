export function StatusDot({ status }: { status: "running" | "stopped" | "checking" }) {
  const styles = {
    running: "bg-emerald-500 shadow-[0_0_6px_rgba(16,185,129,0.6)]",
    stopped: "bg-red-500",
    checking: "bg-amber-500 animate-pulse",
  };

  return (
    <span
      className={`inline-block w-2.5 h-2.5 rounded-full ${styles[status]}`}
      title={status}
    />
  );
}
